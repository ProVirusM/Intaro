<?php
// Определяем функцию для удвоения чисел в кавычках
function doubleQuotedNumbers($input) {
    // Используем preg_replace_callback для замены найденных совпадений
    return preg_replace_callback(
    // Регулярное выражение для поиска чисел в кавычках
        "/'(\d+)'/",
        // Обратный вызов для обработки каждого найденного совпадения
        function($matches) {
            // Удваиваем число, найденное в кавычках, и возвращаем его, окруженное кавычками
            return "'" . ($matches[1] * 2) . "'";
        },
        // Входная строка, в которой будет производиться замена
        $input
    );
}

// Пример использования функции
$input = "'2aaa'''44'bbb'49'sgsdf'124444443'";
// Вызываем функцию для обработки входной строки
$output = doubleQuotedNumbers($input);
// Выводим результат
echo $output;
